func TestFix_HardLineBreakParagraphSkipped(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	// First line ends with a backslash hard break; the paragraph must be
	// left untouched so the intentional break survives.
	src := "This first line is quite long and ends with a hard break here now\\\nsecond line."
	if got := fixSource(t, r, src); got != src {
		t.Errorf("paragraph with hard break must be skipped:\n%q", got)
	}
}

func TestFix_GeneratedSectionSkipped(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "The quick brown fox jumps over the lazy dog and then keeps running for a while."
	f, err := lint.NewFile("test.md", []byte(src))
	require.NoError(t, err)
	// Mark the whole document as a generated range: the host file must
	// not rewrite generated bytes.
	f.GeneratedRanges = []lint.LineRange{{From: 1, To: 1}}
	if got := string(r.Fix(f)); got != src {
		t.Errorf("paragraph inside a generated range must be skipped:\n%q", got)
	}
}

func TestFix_TrailingNewlinePreserved(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "The quick brown fox jumps over the lazy dog and then keeps running for a while.\n"
	got := fixSource(t, r, src)
	assert.True(t, strings.HasSuffix(got, "\n"), "trailing newline must be preserved")
	assert.False(t, strings.HasSuffix(got, "\n\n"), "no spurious trailing blank line")
}

func TestFix_CodeBlockNotReflowed(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "```\nthis is a long line inside a fenced code block that exceeds the configured width\n```\n"
	if got := fixSource(t, r, src); got != src {
		t.Errorf("code block content must not be reflowed:\n%q", got)
	}
}
