package linelength

import (
	"strings"
	"testing"

	"github.com/jeduden/mdsmith/internal/lint"
	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func fixSource(t *testing.T, r *Rule, src string) string {
	t.Helper()
	f, err := lint.NewFile("test.md", []byte(src))
	require.NoError(t, err)
	return string(r.Fix(f))
}

func TestFix_DisabledIsNoOp(t *testing.T) {
	r := &Rule{Max: 40} // Reflow defaults to false
	src := "This line is deliberately made long enough to exceed the forty character limit here.\n"
	if got := fixSource(t, r, src); got != src {
		t.Errorf("Fix with reflow off changed source:\n%q", got)
	}
}

func TestFix_BasicReflow(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "# Title\n\nThe quick brown fox jumps over the lazy dog and then keeps running for a while."
	want := "# Title\n\nThe quick brown fox jumps over the lazy\ndog and then keeps running for a while."
	got := fixSource(t, r, src)
	assert.Equal(t, want, got)
	for _, line := range strings.Split(got, "\n") {
		assert.LessOrEqual(t, len(line), 40, "line exceeds width: %q", line)
	}
}

func TestFix_Idempotent(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "The quick brown fox jumps over the lazy dog and then keeps running for a while longer."
	once := fixSource(t, r, src)
	twice := fixSource(t, r, once)
	assert.Equal(t, once, twice, "reflow is not a fixpoint")
}

func TestFix_ShortParagraphUntouched(t *testing.T) {
	r := &Rule{Max: 80, Reflow: true}
	src := "Short and sweet.\n\nAlso short.\n"
	if got := fixSource(t, r, src); got != src {
		t.Errorf("Fix reflowed paragraphs with no long line:\n%q", got)
	}
}

func TestFix_TwoParagraphsOnlyLongOne(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "Short paragraph here.\n\n" +
		"The quick brown fox jumps over the lazy dog and then keeps running for a while."
	got := fixSource(t, r, src)
	assert.True(t, strings.HasPrefix(got, "Short paragraph here.\n\n"),
		"short paragraph should be preserved verbatim: %q", got)
	for _, line := range strings.Split(got, "\n") {
		assert.LessOrEqual(t, len(line), 40, "line exceeds width: %q", line)
	}
}

func TestFix_AbbreviationKeptWithFollowingWord(t *testing.T) {
	r := &Rule{Max: 12, Reflow: true}
	src := "We met Dr. Watson today."
	// "Dr. Watson" is one unit; it does not fit after "We met", so it
	// wraps whole onto its own line.
	want := "We met\nDr. Watson\ntoday."
	got := fixSource(t, r, src)
	assert.Equal(t, want, got)
	for _, line := range strings.Split(got, "\n") {
		assert.False(t, strings.HasSuffix(line, "Dr."),
			"line must not end with the abbreviation Dr.: %q", line)
	}
}

func TestFix_CodeSpanPreservedAtomically(t *testing.T) {
	r := &Rule{Max: 40, Reflow: true}
	src := "This sentence uses `a  b  c` inline and runs well past the forty char width limit."
	got := fixSource(t, r, src)
	assert.Contains(t, got, "`a  b  c`",
		"code span internal spacing must be preserved: %q", got)
}

